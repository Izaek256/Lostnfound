function getCurrentConfig() {
    global $deployment_configs;
    
    $mode = DEPLOYMENT_MODE;
    if (!isset($deployment_configs[$mode])) {
        echo "Warning: Unknown deployment mode '$mode', falling back to 'local'\n";
        $mode = 'local';
    }
    
    return $deployment_configs[$mode];
}

